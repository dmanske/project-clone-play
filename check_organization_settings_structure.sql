-- Verificar estrutura da tabela organization_settings
SELECT 
    column_name,
    data_type,
    is_nullable,
    column_default
FROM information_schema.columns 
WHERE table_schema = 'public' 
    AND table_name = 'organization_settings'
ORDER BY ordinal_position;

